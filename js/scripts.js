$(document).ready(function(){
    $("#hide-1").hide();
    $(".toggle1, #hide-1").click(function(){
       $(".toggle1").hide(1000); 
        $("#hide-1").show(1000);
});

$("#hide-1").click(function(){
    $(".toggle1").slideDown();
    $("#hide-1").hide(1000);
})
});
